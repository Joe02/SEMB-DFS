#define N_NODES 2000
#define N_TO_REACH 721
    
typedef struct
{
    int label;
    bool visited;
} Vertex;

//stack variables

int stack[N_NODES];
int top = -1;
int counter = -1;
int bestSolution = 999999;
int lineCounter = 0;
//graph variables

//array of vertices
Vertex lstVertices[N_NODES];

//adjacency matrix
int adjMatrix[N_NODES][N_NODES];

//vertex count
int vertexCount = 0;

//stack functions

void push(int item)
{
    stack[++top] = item;
}

int pop()
{
    return stack[top--];
}

int peek()
{
    return stack[top];
}

bool isStackEmpty()
{
    return top == -1;
}

//graph functions

//add vertex to the vertex list
void addVertex(int label)
{
    Vertex vertex;
    vertex.label = label;
    vertex.visited = false;
    lstVertices[vertexCount++] = vertex;
}

//add edge to edge array
void addEdge(int start, int end)
{
    adjMatrix[start][end] = 1;
    // adjMatrix[end][start] = 1;
}

//display the vertex
void displayVertex(int vertexIndex)
{
    printf("%03d ", lstVertices[vertexIndex].label);
}

//get the adjacent unvisited vertex
int getAdjUnvisitedVertex(int vertexIndex, int baseCount)
{
    int i;

    for (i = 0; i < vertexCount; i++)
    {
        if (adjMatrix[vertexIndex][i] == 1)
        {
            if (lstVertices[i].visited == false) {
                return i;
            }
            
            if (lstVertices[i].label == N_TO_REACH) {
                // printf("baseCount=%d, best=%d", baseCount+1, bestSolution);
                if (baseCount+1 < bestSolution) {
                    bestSolution = baseCount+1;
                    if (baseCount == 0) {
                        displayVertex(0);
                    }
                    displayVertex(i);
                }
            }
        }
    }

    return -1;
}

void depthFirstSearch()
{
    int i;

    //mark first node as visited
    lstVertices[0].visited = true;

    // //display the vertex
    // displayVertex(0);

    //push vertex index in stack
    push(0);

    while (!isStackEmpty())
    {
        //get the unvisited vertex of vertex which is at top of the stack
        // printf("top=%d\n", top);
        int unvisitedVertex = getAdjUnvisitedVertex(peek(), top);

        //no adjacent vertex found
        if (unvisitedVertex == -1)
        {
            pop();
            if (top == 0) {
                printf("\n");
            }
        }
        else
        {
            lineCounter++;
            if (lstVertices[unvisitedVertex].label == N_TO_REACH) {
                
                if (top+1 < bestSolution) {
                    bestSolution = top+1;
                }
            }
            if (top == 0) {
                displayVertex(0);
            }
            if (lineCounter > 20) {
                lineCounter = 0;
                printf(" -> \n->");
            }
            lstVertices[unvisitedVertex].visited = true;
            displayVertex(unvisitedVertex);
            push(unvisitedVertex);
        }
    }

    //stack is empty, search is complete, reset the visited flag
    for (i = 0; i < vertexCount; i++)
    {
        lstVertices[i].visited = false;
    }
}

